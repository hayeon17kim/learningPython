# 불린 (Boolean)
print(True)
print(False)
# and
print(True and True)
print(True and False)
print(False and True)
print(False and False)
# or
print(True or True)
print(True or False)
print(False or True)
print(False or False)
# 명제
print("Hello" == "Hello") # True
print("Hello" != "Hello") # False
# not
print(not not True)
print(not not False)